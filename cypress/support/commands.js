Cypress.Commands.add('randomEmail', () => {

    cy.writeFile('cypress/fixtures/example.json', {
        'hits':Cypress._.times(1, () => {
            let randomNumber= Math.floor(Math.random() * 1001);
            const email ='random+'+randomNumber+'@mail.com';
            return {
                'Email': email
    
            }
        })
})
})
